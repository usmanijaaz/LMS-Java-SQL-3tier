    private void showbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showbuttonActionPerformed
        // TODO add your handling code here:
        String course = clist.getSelectedItem();
        ArrayList<Attendance> atd = new ArrayList<Attendance>();
        
         ArrayList<String> Crs_id = conobject.getCourse(course);  
        Course crs = new Course(Integer.parseInt(Crs_id.get(0)),Crs_id.get(2),Crs_id.get(1),Integer.parseInt(Crs_id.get(3)),Integer.parseInt(Crs_id.get(4)));
        

        
               atd = gc.currentUser.viewAttendance(crs);
                
       
        model.clear();
        model.addElement("     Date      |  Presence  ");
        if(atd.size() == 0)
        {
            model.addElement("     Nothing to Show!   ");
            attlist.setModel(model);
        }
             else
        {
            int a =0;
        for(int i=0;i<atd.size();i++)
        {
            if(atd.get(i).getStid() == gc.currentUser.getId() && atd.get(i).getsem() == gc.currentUser.getSemester_No())
            {
            model.addElement("  " + atd.get(i).getDate() + "  |   " + atd.get(i).getstatus()); 
            a++;
            }
            
        }
        if(a==0)
        {
             model.addElement("     Nothing to Show!   ");
        }
        
        attlist.setModel(model);
        }
    }//GEN-LAST:event_showbuttonActionPerformed

