    public void showAttendance()
    {
        rolinput.setText(String.valueOf(gc.currentUser.getId()));
        
        
        
        clist.removeAll();
        
            for(int i=0;i<gc.currentUser.getCourses().size();i++)
             {
                 if(gc.currentUser.getCourses().get(i).getSemester_no() == gc.currentUser.getSemester_No())
                 {
                 clist.add(gc.currentUser.getCourses(i).getCourseName());           
                 } 
             } 
             for(int i=0;i<gc.currentUser.getTrans().getCourses().size();i++)
            {
              if(gc.currentUser.getTrans().getGrades().get(i).equals("F") && gc.currentUser.getCourses().get(i).getSemester_no() == (gc.currentUser.getSemester_No()-1))
               {
                 clist.add(gc.currentUser.getCourses(i).getCourseName());  
               }
            }
            
            model.addElement("     Date      |  Presence  ");
             attlist.setModel(model);
    }
    
    
