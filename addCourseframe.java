    private void adbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adbuttonActionPerformed
        // TODO add your handling code here:
        
        //cid.getText(); cname.getText() ; ccode.getText(); chr.getText(); csem.getText();
        
        try 
        {
            String Name = cname.getText();
            String code = ccode.getText();
            if(Name.length() == 0 || code.length() == 0 || cid.getText().length()==0 || chr.getText().length() == 0 || csem.getText().length() == 0)
            {
                 JOptionPane.showMessageDialog(null,"Enter data in all Fields ! ");
            }
            else
            {
            int id = Integer.parseInt(cid.getText());
            int cr = Integer.parseInt(chr.getText());
            int sem = Integer.parseInt(csem.getText());
           
            Course newCourse = new Course(id,Name,code,cr,sem);
            int numberofSections = Integer.parseInt(csec.getSelectedItem());
            
            
           int r = conobject.addaNewCourse(newCourse,numberofSections);
           if(r == -1)
           {
                JOptionPane.showMessageDialog(null,"Each Course Id should be Unique, this Id already exists ! ");
           }
           else
           {
               this.setVisible(false);
               this.makeFrame();
               this.setVisible(true);
               
           }
                
            }
        }
        catch(NumberFormatException e)
        {
            JOptionPane.showMessageDialog(null,"Enter only integer in integer field !! ");
        }
        
    }//GEN-LAST:event_adbuttonActionPerformed

