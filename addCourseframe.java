    public void makeFrame()
    {
        rolinput.setText(String.valueOf(gc.officer.getId()));
        ArrayList<String> all = conobject.viewAllCourses();
        
        DefaultListModel mod = new DefaultListModel();
        for(int i=0;i<all.size();i++)
        {
            mod.addElement(all.get(i));
        }
        
        clist.setModel(mod);
        
        csec.removeAll();
        for(int i=1;i<4;i++)
        {
            csec.add(String.valueOf(i));
        }
    }
    
    
